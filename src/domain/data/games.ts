/**
 * Get games by category
 */
export function getGamesByCategory(category: GameCategoryKey): GameMeta[] {
  return GAMES.filter((game) => game.category === category);
}

/**
 * Get game by slug
 */
export function getGameBySlug(slug: string): GameMeta | undefined {
  return GAMES.find((game) => game.slug === slug);
}

/**
 * Get all categories with game counts
 */
export function getCategoriesWithCounts(): Array<{
  key: GameCategoryKey;
  name: string;
  nameTh: string;
  icon: string;
  color: string;
  count: number;
}> {
  return (Object.keys(GAME_CATEGORIES) as GameCategoryKey[]).map((key) => ({
    key,
    ...GAME_CATEGORIES[key],
    count: getGamesByCategory(key).length,
  }));
}

/**
 * Search games by name
 */
export function searchGames(query: string): GameMeta[] {
  const lowerQuery = query.toLowerCase();
  return GAMES.filter(
    (game) =>
      game.name.toLowerCase().includes(lowerQuery) ||
      game.nameTh?.toLowerCase().includes(lowerQuery) ||
      game.slug.includes(lowerQuery)
  );
}
